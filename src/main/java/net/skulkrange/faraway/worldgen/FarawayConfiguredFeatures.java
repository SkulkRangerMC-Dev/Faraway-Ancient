import java.util.List;

public class FarawayConfiguredFeatures {
    public static final ResourceKey<ConfiguredFeature<?, ?>> ANCIENT_RESIN_ORE_KEY = registerKey("ancient_resin_ore");

    public static void bootstrap(BootstapContext<ConfiguredFeature<?, ?>> context) {
        RuleTest stoneReplaceable = new TagMatchTest(BlockTags.STONE_ORE_REPLACEABLES);
        RuleTest deepslateReplaceables = new TagMatchTest(BlockTags.DEEPSLATE_ORE_REPLACEABLES);

        List<OreConfiguration.TargetBlockState> ancientResinOres = List.of(OreConfiguration.target(stoneReplaceable,
                        FarawayAncientBlocks.ANCIENT_RESIN_ORE.get().defaultBlockState()),
                OreConfiguration.target(deepslateReplaceables, FarawayAncientBlocks.ANCIENT_RESIN_ORE.get().defaultBlockState()));

        register(context, ANCIENT_RESIN_ORE_KEY, Feature.ORE, new OreConfiguration(ancientResinOres, 9));
    }


    public static ResourceKey<ConfiguredFeature<?, ?>> registerKey(String name) {
        return ResourceKey.create(Registries.CONFIGURED_FEATURE, new ResourceLocation(FarawayMod.MOD_ID, name));
    }

    private static <FC extends FeatureConfiguration, F extends Feature<FC>> void register(BootstapContext<ConfiguredFeature<?, ?>> context,
                                                                                          ResourceKey<ConfiguredFeature<?, ?>> key, F feature, FC configuration) {
        context.register(key, new ConfiguredFeature<>(feature, configuration));
    }
}